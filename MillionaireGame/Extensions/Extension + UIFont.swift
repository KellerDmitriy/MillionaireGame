import UIKit.UIFont

extension UIFont {
    
    //Regular
    static func syneRegular50() -> UIFont? {
        UIFont.init(name: "Syne-Regular", size: 50)
    }
    
    
    //Medium
    static func robotoMedium24() -> UIFont? {
        UIFont.init(name: "Roboto-Medium", size: 24)
    }
    
    static func robotoMedium36() -> UIFont? {
        UIFont.init(name: "Roboto-Medium", size: 36)
    }
}
